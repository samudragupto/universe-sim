TextRenderer::TextRenderer() : m_vao(0), m_vbo(0), m_program(0), m_fontTexture(0),
    m_screenWidth(1920), m_screenHeight(1080) {}

TextRenderer::~TextRenderer() { cleanup(); }

void TextRenderer::init(int screenWidth, int screenHeight) {
    m_screenWidth = screenWidth;
    m_screenHeight = screenHeight;

    GLuint vs = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(vs, 1, &textVertSrc, nullptr);
    glCompileShader(vs);

    GLuint fs = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(fs, 1, &textFragSrc, nullptr);
    glCompileShader(fs);

    m_program = glCreateProgram();
    glAttachShader(m_program, vs);
    glAttachShader(m_program, fs);
    glLinkProgram(m_program);
    glDeleteShader(vs);
    glDeleteShader(fs);

    glGenVertexArrays(1, &m_vao);
    glGenBuffers(1, &m_vbo);

    glBindVertexArray(m_vao);
    glBindBuffer(GL_ARRAY_BUFFER, m_vbo);

    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(2 * sizeof(float)));
    glEnableVertexAttribArray(2);
    glVertexAttribPointer(2, 4, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(4 * sizeof(float)));

    glBindVertexArray(0);

    createFontTexture();
}

void TextRenderer::createFontTexture() {
    int texW = 8 * 16;
    int texH = 8 * 6;
    unsigned char* pixels = new unsigned char[texW * texH];
    memset(pixels, 0, texW * texH);

    for (int c = 0; c < 95; c++) {
        int col = c % 16;
        int row = c / 16;
        for (int y = 0; y < 8; y++) {
            unsigned char line = fontBitmap[c][y];
            for (int x = 0; x < 8; x++) {
                if (line & (0x80 >> x)) {
                    int px = col * 8 + x;
                    int py = row * 8 + y;
                    pixels[py * texW + px] = 255;
                }
            }
        }
    }

    glGenTextures(1, &m_fontTexture);
    glBindTexture(GL_TEXTURE_2D, m_fontTexture);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_R8, texW, texH, 0, GL_RED, GL_UNSIGNED_BYTE, pixels);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

    delete[] pixels;
}

void TextRenderer::resize(int w, int h) {
    m_screenWidth = w;
    m_screenHeight = h;
}

void TextRenderer::begin() {
    m_entries.clear();
    m_vertices.clear();
}

void TextRenderer::drawText(const std::string& text, float x, float y,
                             float scale, float r, float g, float b, float a) {
    m_entries.push_back({text, x, y, r, g, b, a, scale});
}

void TextRenderer::end() {
    m_vertices.clear();

    float glyphW = 8.0f;
    float glyphH = 8.0f;
    float texW = 8.0f * 16.0f;
    float texH = 8.0f * 6.0f;

    for (auto& entry : m_entries) {
        float cx = entry.x;
        float cy = entry.y;
        float s = entry.scale;

        for (char ch : entry.text) {
            if (ch == '\n') {
                cx = entry.x;
                cy += glyphH * s + 2.0f * s;
                continue;
            }

            int idx = (int)ch - 32;
            if (idx < 0 || idx >= 95) idx = 0;

            int col = idx % 16;
            int row = idx / 16;

            float u0 = (col * glyphW) / texW;
            float v0 = (row * glyphH) / texH;
            float u1 = ((col + 1) * glyphW) / texW;
            float v1 = ((row + 1) * glyphH) / texH;

            float x0 = cx;
            float y0 = cy;
            float x1 = cx + glyphW * s;
            float y1 = cy + glyphH * s;

            float r = entry.r, g = entry.g, b = entry.b, a = entry.a;

            m_vertices.insert(m_vertices.end(), {x0,y0, u0,v0, r,g,b,a});
            m_vertices.insert(m_vertices.end(), {x1,y0, u1,v0, r,g,b,a});
            m_vertices.insert(m_vertices.end(), {x1,y1, u1,v1, r,g,b,a});
            m_vertices.insert(m_vertices.end(), {x0,y0, u0,v0, r,g,b,a});
            m_vertices.insert(m_vertices.end(), {x1,y1, u1,v1, r,g,b,a});
            m_vertices.insert(m_vertices.end(), {x0,y1, u0,v1, r,g,b,a});

            cx += glyphW * s;
        }
    }

    if (m_vertices.empty()) return;

    glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
    glBufferData(GL_ARRAY_BUFFER, m_vertices.size() * sizeof(float), m_vertices.data(), GL_DYNAMIC_DRAW);

    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
    glDisable(GL_DEPTH_TEST);

    glUseProgram(m_program);
    glUniform2f(glGetUniformLocation(m_program, "uScreenSize"), (float)m_screenWidth, (float)m_screenHeight);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, m_fontTexture);
    glUniform1i(glGetUniformLocation(m_program, "uFont"), 0);

    glBindVertexArray(m_vao);
    glDrawArrays(GL_TRIANGLES, 0, (GLsizei)(m_vertices.size() / 8));
    glBindVertexArray(0);

    glEnable(GL_DEPTH_TEST);
}

void TextRenderer::cleanup() {
    if (m_vao) glDeleteVertexArrays(1, &m_vao);
    if (m_vbo) glDeleteBuffers(1, &m_vbo);
    if (m_program) glDeleteProgram(m_program);
    if (m_fontTexture) glDeleteTextures(1, &m_fontTexture);
    m_vao = 0; m_vbo = 0; m_program = 0; m_fontTexture = 0;
}